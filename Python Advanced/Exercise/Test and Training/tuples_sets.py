# Могат да се променят елементи в тюпъл() само ако в него са от тип листове - [] или речници {}
tuple = (

    [1,2,3],
    {},
    (4,5,6)
)

print(tuple) # ([1, 2, 3], {}, (4, 5, 6))

tuple[0].append(-4)
tuple[1]['Lachezar'] = 'Indarov'

print(tuple) # ([1, 2, 3, -4], {'Lachezar': 'Indarov'}, (4, 5, 6))

